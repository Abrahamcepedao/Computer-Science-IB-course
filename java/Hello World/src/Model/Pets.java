package Model;

public class Pets {
	public String name;
	public String type;
	public int age;
	public String gender;
	public String color;
	public int size;
	public Pets(String name, String type, int age, String gender, String color, int size) {
		this.name = name;
		this.type = type;
		this.age = age;
		this.gender = gender;
		this.color = color;
		this.size = size;
	}	
	
}
